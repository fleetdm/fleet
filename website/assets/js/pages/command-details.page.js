      let code = $(this).closest('[purpose="codeblock"]').find('pre:visible code').text();
      if(code) {
        $(this).addClass('copied');
        await setTimeout(()=>{
          $(this).removeClass('copied');
        }, 2000);
        navigator.clipboard.writeText(code);
      }
